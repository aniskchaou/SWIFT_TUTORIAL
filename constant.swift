//Constants refer to fixed values that a program may not alter during its execution. 

//Constants Declaration
//Before you use constants, you must declare them using let keyword as follows 
//let constantName = <initial value>

let constA = 42
print(constA)

//Type Annotations
//You can provide a type annotation when you declare a constant, to be clear about the kind of values the constant can store.

//var constantName:<data type> = <optional initial value>

let constA = 42
print(constA)

let constB:Float = 3.14159
print(constB)


//Printing Constants
//You can print the current value of a constant or variable using print function. 
let constA = "Godzilla"
let constB = 1000.00
print("Value of \(constA) is more than \(constB) millions")
